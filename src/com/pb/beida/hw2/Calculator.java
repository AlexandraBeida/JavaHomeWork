package com.pb.beida.hw2;

public class Calculator {
    public static void main(String[] args) {
        int operand1 = 10;
        int operand2 = 20;
        System.out.println("a + b = " + (operand1 + operand2));
        System.out.println("a - b = " + (operand1 - operand2));
        System.out.println("a * b = " + (operand1 * operand2));
        System.out.println("b / a = " + (operand2 / operand1));
    }
}
